func amountToFloat(balance string, decimals int64) float64 {
	decimalBigInt := new(big.Int).Mul(big.NewInt(1), big.NewInt(decimals))
	balanceBI, _ := new(big.Int).SetString(balance, 10)
	balanceF, _ := new(big.Float).SetPrec(1000000).Quo(new(big.Float).SetInt(balanceBI), new(big.Float).SetInt(decimalBigInt)).Float64() //converting to KAI from HYDRO
	return balanceF
}
